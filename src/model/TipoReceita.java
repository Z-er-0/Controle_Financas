package model;

/**
 * A enumeração TipoReceita representa os tipos de receita.
 * Pode ser SALARIO, DECIMO, FERIAS ou OUTRA.
 */
public enum TipoReceita {
    SALARIO, // Receita de salário
    DECIMO, // Receita de décimo terceiro
    FERIAS, // Receita de férias
    OUTRA; // Outro tipo de receita
}
