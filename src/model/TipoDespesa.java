package model;

/**
 * A enumeração TipoDespesa representa os tipos de despesa.
 * Pode ser ALIMENTACAO, TRANSPORTE, RESIDENCIA, SAUDE, EDUCACAO, ENTRETENIMENTO ou OUTRA.
 */
public enum TipoDespesa {
    ALIMENTACAO, // Despesa de alimentação
    TRANSPORTE, // Despesa de transporte
    RESIDENCIA, // Despesa de residência
    SAUDE, // Despesa de saúde
    EDUCACAO, // Despesa de educação
    ENTRETENIMENTO, // Despesa de entretenimento
    OUTRA; // Outro tipo de despesa
}
